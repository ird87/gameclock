def get_short_url(id):
    id_str = str(id)
    if len(id_str) % 2 == 0:
        items = re.findall(r'.{2}', id_str)
    else:
        last_id_str = id_str[-1:]
        other_id_str = id_str[:-1]
        items = re.findall(r'.{2}', other_id_str)
        items.append(last_id_str)
    short_url = ""
    for item in items:
        short_url += list_conversions[item]
    return short_url


